package cjd;

public class CJDException extends Exception {

	public CJDException() {
	}

	public CJDException(String message) {
		super(message);
	}

	public CJDException(Throwable cause) {
		super(cause);
	}

	public CJDException(String message, Throwable cause) {
		super(message, cause);
	}

}
